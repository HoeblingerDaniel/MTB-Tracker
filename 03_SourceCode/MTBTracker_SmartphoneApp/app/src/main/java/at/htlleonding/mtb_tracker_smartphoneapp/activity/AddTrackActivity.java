package at.htlleonding.mtb_tracker_smartphoneapp.activity;

import androidx.annotation.NonNull;
import androidx.appcompat.app.AppCompatActivity;
import androidx.core.app.ActivityCompat;

import android.Manifest;
import android.content.pm.PackageManager;
import android.location.Location;
import android.os.Build;
import android.os.Bundle;
import android.widget.Button;
import android.widget.TextView;
import android.widget.Toast;

import com.google.android.gms.location.FusedLocationProviderClient;
import com.google.android.gms.location.LocationRequest;
import com.google.android.gms.location.LocationServices;
import com.google.android.gms.tasks.OnSuccessListener;

import at.htlleonding.mtb_tracker_smartphoneapp.R;

public class AddTrackActivity extends AppCompatActivity {
    private static final int PERMISSIONS_FINE_LOCATION = 99;
    FusedLocationProviderClient mFLPC;
    LocationRequest mLocationRequest;
    TextView lat = findViewById(R.id.lati);
    TextView lon = findViewById(R.id.longitude);
    Button backButton = findViewById(R.id.backBTN);
    Button addTrackButton = findViewById(R.id.startGPSBTN);

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_add_track);

        mLocationRequest = new LocationRequest();
        mLocationRequest.setInterval(30000);
        mLocationRequest.setFastestInterval(5000);
        mLocationRequest.setPriority(LocationRequest.PRIORITY_HIGH_ACCURACY); //GPS accuracy

        backButton.setOnClickListener(e-> setContentView(R.layout.activity_main)); //I really dont know why its not working

        addTrackButton.setOnClickListener(e-> onAddTrackButtonClick());
    }


    @Override
    public void onRequestPermissionsResult(int requestCode, @NonNull String[] permissions, @NonNull int[] grantResults) {
        super.onRequestPermissionsResult(requestCode, permissions, grantResults);

        switch (requestCode) {
            case PERMISSIONS_FINE_LOCATION:
                if (grantResults[0] == PackageManager.PERMISSION_GRANTED) {
                    updateGPS();
                }
                else {
                    Toast.makeText(AddTrackActivity.this, "Permissions required!", Toast.LENGTH_SHORT).show();
                    finish();
                }
                break;
        }
    }

    private void onAddTrackButtonClick() {
        updateGPS();
    }

    private void updateGPS() {
        mFLPC = LocationServices.getFusedLocationProviderClient(this);
        if (ActivityCompat.checkSelfPermission(this, Manifest.permission.ACCESS_FINE_LOCATION) == PackageManager.PERMISSION_GRANTED) {
            mFLPC.getLastLocation().addOnSuccessListener(this, new OnSuccessListener<Location>() {
                @Override
                public void onSuccess(Location location) {
                    UpdateUIValues(location);
                }
            });
        }
        else {
            if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.M) {
                requestPermissions(new String[] {Manifest.permission.ACCESS_FINE_LOCATION}, PERMISSIONS_FINE_LOCATION);
            }
        }
    }

    private void UpdateUIValues(Location location) {
        //Where to store data?
        lat.setText(String.valueOf(location.getLatitude()));
        lon.setText(String.valueOf(location.getLongitude()));
    }
}








































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































